
package Model;

public class Pelanggan {
    private String kdPlg;
    private String nmPlg;
    private String alamatPlg;
    private String telpPlg;

    public String getKdPlg() {
        return kdPlg;
    }
    public void setKdPlg(String kdPlg) {
        this.kdPlg = kdPlg;
    }
    public String getNmPlg() {
        return nmPlg;
    }
    public void setNmPlg(String nmPlg) {
        this.nmPlg = nmPlg;
    }
    public String getAlamatPlg() {
        return alamatPlg;
    }
    public void setAlamatPlg(String alamatPlg) {
        this.alamatPlg = alamatPlg;
    }
    public String getTelpPlg() {
        return telpPlg;
    }
    public void setTelpPlg(String telpPlg) {
        this.telpPlg = telpPlg;
    } 
    
    
}
